function traduction_lien($lien){
	$return = $lien;
	if($lien == "/"){
		$return = "Accueil";
	}
	
	if(strpos(".",$return) !== false){
		$return = strstr(".",$return,1);
	}
	if(strpos("/",$return) !== false){
		$return = strstr("/",$return);
	}
	$return = global_class::tronquer($return, 30, "", "-");
	
	return $return;
}